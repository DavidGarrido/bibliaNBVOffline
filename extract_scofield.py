def clean_osis(text):
    """Elimina tags OSIS/XML y limpia el texto."""
    # Eliminar el sufijo "(Scofield)"
    text = re.sub(r'\s*\(Scofield\)\s*$', '', text.strip())
    # Convertir referencias a texto plano
    text = re.sub(r'<reference[^>]*>([^<]*)</reference>', r'\1', text)
    # Eliminar todos los tags restantes
    text = re.sub(r'<[^>]+/?>', '', text)
    # Limpiar espacios múltiples
    text = re.sub(r'  +', ' ', text)
    text = text.strip()
    return text


def get_note(book_abbr, chap, verse):
    """Llama a diatheke y retorna el texto limpio, o None si no hay nota."""
    key = f"{book_abbr} {chap}:{verse}"
    result = subprocess.run(
        ['diatheke', '-b', 'Scofield', '-f', 'plain', '-k', key],
        capture_output=True, text=True, timeout=10
    )
    out = result.stdout.strip()
    # Si solo devuelve la referencia sin contenido real, no hay nota
    if not out or out.startswith(f"{key}:  (Scofield)") or '(Scofield)' not in out:
        return None
    # Extraer contenido después de "Book Cap:Verse: "
    prefix_end = out.find(': ', len(key))
    if prefix_end != -1:
        content = out[prefix_end+2:]
    else:
        content = out
    cleaned = clean_osis(content)
    return cleaned if cleaned else None


def main():
    out_path = Path(__file__).parent / 'commentary-scofield.json'
    notes = {}
    total_books = len(BOOKS)

    for book_id, abbr, num_chaps in BOOKS:
        print(f"[{book_id:2d}/{total_books}] {abbr}...", end=' ', flush=True)
        book_notes = 0
        chapters = VERSES_PER_CHAPTER.get(book_id, [])
        for chap_idx, num_verses in enumerate(chapters, 1):
            for verse in range(1, num_verses + 1):
                note = get_note(abbr, chap_idx, verse)
                if note:
                    key = f"{book_id}_{chap_idx}_{verse}"
                    notes[key] = note
                    book_notes += 1
        print(f"{book_notes} notas")

    print(f"\nTotal: {len(notes)} notas")
    with open(out_path, 'w', encoding='utf-8') as f:
        json.dump(notes, f, ensure_ascii=False, separators=(',', ':'))
    size_kb = out_path.stat().st_size / 1024
    print(f"Guardado en {out_path} ({size_kb:.0f} KB)")


if __name__ == '__main__':
    main()
